namespace CommandPattern.FirstImplementation;

public class Light
{
    public Light()
    {
        
    }

    public void On()
    {
        Console.WriteLine("Light Is On");
    }

    public void Off()
    {
        Console.WriteLine("Light Is Off");
    }

    public void Blink()
    {
        Console.WriteLine("Light is Blinking");
    }
}
